#Some are the examples of loops in Python
#Example 1: Basic for loop
numbers = [1, 2, 3, 4, 5]
for num in numbers:
    print(num * 2)

#Example 2: While loop
count = 0
while count < 5:
    print(f"Count is {count}")
    count += 1

#Example 3: For loop with range
for i in range(10):
    if i % 2 == 0:
        print(i)
